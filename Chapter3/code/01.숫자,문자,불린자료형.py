# 주석 (#)으로 작성, 영어로는 (Comments)
# 1. 코드에 설명을 추가하고 싶을 때
# 2. 코드를 실행하고 싶지 않을 때

# print("hello world")  명령을 실행하고 싶지 않을 때.

# - 숫자 자료형
# 1. 정수형 : 소수점이 없는 수
print(1, 3, 5, -1) # 자동으로 출력문에 blank(스페이스 바, 공배)이 생긴다.
print(1, 3, 5, -1, sep="") #  sep 공백 문자

# 2. 실수형 : 소수점이 있는 정수형을 포함하는 수
print(1.1, -0.5)

# - 문자열 자료형
# "" or ''
print("Hello World", end='')
print('Hello World~')
print("'개 짖는 소리 좀 안나게 해라' 라고 그는 외쳤다.")

# - 불린형 자료형
# True or False
print(True)
print(False)